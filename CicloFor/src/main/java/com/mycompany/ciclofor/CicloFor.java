/*
    Ciclo for
    
    for(inicialización ; condición ; aumento o decremento){
        Instrucciones;
    }
 */

package com.mycompany.ciclofor;

/**
 *
 * @author noral
 */
public class CicloFor {

    public static void main(String[] args) {
        
        for(int i=1; i<=10; i++){
            System.out.println(i);
        }
    }
}
